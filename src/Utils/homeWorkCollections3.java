package Utils;

import java.util.List;

public class homeWorkCollections3 {

       private String name;
       private String surname;
       private int age;
       private String city;

    public homeWorkCollections3(String name, String surname, int age, String city) {
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.city = city;
    }

    public String getName() {
        return name;
    }

    public String getSurname() {
        return surname;
    }

    public int getAge() {
        return age;
    }

    public String getCity() {
        return city;
    }
}
